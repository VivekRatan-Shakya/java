public class number_pattern 
{
    public static void number(int n)
    {
        for(int i=1;i<=n;i++)
   {
      for(int j=1;j<=n-i;j++)
   {
    System.out.print(" ");
   }
   for(int j=1;j<=i;j++)
   {
    System.out.print( " "+i);
   }
   {
    System.out.println();
   }
}}
   public static void main(String []args)
   {
    number(5);
   }
}
    




























